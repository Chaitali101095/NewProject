package variables;

public class SwitchCase1 { 

	public static void main(String[] args) 
	{
		int n=70;
		switch(n)
		{
		case 10:
			System.out.println("i am 10");
			
		case 20:
			System.out.println("i am 20");
			
		case 30:
			System.out.println("i am 30");
			
		case 40:
			System.out.println("i am 40");
			
		case 50:
			System.out.println("i am 50");
			break;
			
		case 60:
			System.out.println("i am 60");
			
		case 70:
			System.out.println("i am 70");
			
		case 80:
			System.out.println("i am 80");
			
		case 90:
			System.out.println("i am 90");
			
			
		default:
				System.out.println("no one is matching");
				
		}
				System.out.println("this belongs to main method");
				
				
	} 

}
